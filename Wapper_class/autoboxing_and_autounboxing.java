package Wapper_class;

public class autoboxing_and_autounboxing {

	public static void main(String[] args) {
//		autoboxing and autounboxing is same as boxing and unboxing => it is usedafter or above 1.5 version
// autoboxing
int a=10;
Integer l=a;
// auto-unboxing
l.toString();  // converts integer to string
int b=l;
System.out.println(b);
		
	}

}
